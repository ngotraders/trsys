import { Edit, useAutocomplete } from "@refinedev/mui";
import { Box, TextField, Autocomplete } from "@mui/material";
import { useForm } from "@refinedev/react-hook-form";
import { IResourceComponentsProps, useTranslate } from "@refinedev/core";
import { Controller } from "react-hook-form";

export const BlogPostEdit: React.FC<IResourceComponentsProps> = () => {
  const translate = useTranslate();
  const {
    saveButtonProps,
    refineCore: { queryResult },
    register,
    control,
    formState: { errors },
  } = useForm();

  const blogPostsData = queryResult?.data?.data;

  const { autocompleteProps: categoryAutocompleteProps } = useAutocomplete({
    resource: "categories",
    defaultValue: blogPostsData?.category?.id,
  });

  return (
    <Edit saveButtonProps={saveButtonProps}>
      <Box
        component="form"
        sx={{ display: "flex", flexDirection: "column" }}
        autoComplete="off"
      >
        <TextField
          {...register("id", {
            required: "This field is required",
            valueAsNumber: true,
          })}
          error={!!errors?.id}
          helperText={errors?.id?.message as string | undefined}
          margin="normal"
          fullWidth
          InputLabelProps={{ shrink: true }}
          type="number"
          label={translate("blog_posts.fields.id")}
          name="id"
          disabled
        />
        <TextField
          {...register("title", {
            required: "This field is required",
          })}
          error={!!errors?.title}
          helperText={errors?.title?.message as string | undefined}
          margin="normal"
          fullWidth
          InputLabelProps={{ shrink: true }}
          type="text"
          label={translate("blog_posts.fields.title")}
          name="title"
        />
        <TextField
          {...register("content", {
            required: "This field is required",
          })}
          error={!!errors?.content}
          helperText={errors?.content?.message as string | undefined}
          margin="normal"
          fullWidth
          InputLabelProps={{ shrink: true }}
          multiline
          label={translate("blog_posts.fields.content")}
          name="content"
        />
        <Controller
          control={control}
          name="category"
          rules={{ required: "This field is required" }}
          // eslint-disable-next-line
          defaultValue={null}
          render={({ field }) => (
            <Autocomplete
              {...categoryAutocompleteProps}
              {...field}
              onChange={(_, value) => {
                field.onChange(value);
              }}
              getOptionLabel={(item) => {
                return (
                  categoryAutocompleteProps?.options?.find(
                    (p) => p?.id?.toString() === item?.id?.toString()
                  )?.title ?? ""
                );
              }}
              isOptionEqualToValue={(option, value) =>
                value === undefined ||
                option?.id?.toString() === value?.id?.toString()
              }
              renderInput={(params) => (
                <TextField
                  {...params}
                  label={translate("blog_posts.fields.category")}
                  margin="normal"
                  variant="outlined"
                  error={
                    !!(
                      errors?.category as
                        | { id: { message: string } }
                        | undefined
                    )?.id
                  }
                  helperText={
                    (
                      errors?.category as
                        | { id: { message: string } }
                        | undefined
                    )?.id?.message
                  }
                  required
                />
              )}
            />
          )}
        />
        <TextField
          {...register("status", {
            required: "This field is required",
          })}
          error={!!errors?.status}
          helperText={errors?.status?.message as string | undefined}
          margin="normal"
          fullWidth
          InputLabelProps={{ shrink: true }}
          type="text"
          label={translate("blog_posts.fields.status")}
          name="status"
        />
        {/*
                    DatePicker component is not included in "@refinedev/mui" package.
                    To use a <DatePicker> component, you can follow the official documentation for Material UI.

                    Docs: https://mui.com/x/react-date-pickers/date-picker/#basic-usage
                */}
        <TextField
          {...register("createdAt", {
            required: "This field is required",
          })}
          error={!!errors?.createdAt}
          helperText={errors?.createdAt?.message as string | undefined}
          margin="normal"
          fullWidth
          InputLabelProps={{ shrink: true }}
          label={translate("blog_posts.fields.createdAt")}
          name="createdAt"
        />
      </Box>
    </Edit>
  );
};
